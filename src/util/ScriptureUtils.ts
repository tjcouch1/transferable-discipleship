/**
 * Gets the book ID from the English book name.
 * @param number - Book English name
 * @returns The book ID if found, or `'***'` otherwise.
 */
export function englishNameToBookId(englishName: string): string {
  const bookNumber = allBookEnglishNames.indexOf(englishName) + 1;
  if (bookNumber <= 0) {
    return "***";
  }

  return Canon.bookNumberToId(bookNumber);
}
